        <p style="color: #666;font-size: 12px;line-height:20px;margin-bottom: 22px;">站内信”是为方便会员商务信件往来而设的服务功能，类似于邮箱，主要由收件箱、发件箱、草稿箱和垃圾箱四部分组成，但该功能仅对网站的注册会员开放。</p>
        <p style="color: #666;font-size: 12px;line-height:20px;margin-bottom: 22px;">“站内信”不同于电子邮件，电子邮件通过专门的邮件服务器发送、保存。而“站内信”是系统内的消息，其实就是通过数据库插入记录来实现的。</p>
        <p style="color: #666;font-size: 12px;line-height:20px;margin-bottom: 22px;">“站内信”有两个基本功能。一：点到点的消息传送。用户给用户发送站内信，管理员给用户发送站内信。二：点到面的消息传送。管理员给用户（指定满足某一条件的用户群）群发消息。</p>
